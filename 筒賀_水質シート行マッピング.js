// W{n}の絶対行 = baseRow + (n-1) * 52
function getWaterRow(weekIndex, itemKey) {
  const baseRow = WATER_USAGE_ROWS_W1[itemKey] ?? WATER_MEASURE_ROWS_W1[itemKey];
  if (baseRow == null) throw new Error("Unknown water item: " + itemKey);
  return baseRow + WATER_WEEK_OFFSET * weekIndex;
}

