// ─── 5週分の絶対行を取得 ───
function getElecRow(weekIndex, subId, itemKey) {
  // weekIndex: 0-4 (W1=0, ..., W5=4)
  // subId: "sub1" | "sub2" | "sub3"
  const map = subId === "sub1" ? ELEC1_ROW_W1
            : subId === "sub2" ? ELEC2_ROW_W1
            : ELEC3_ROW_W1;
  const baseRow = map[itemKey];
  if (baseRow == null) {
    throw new Error(`Unknown elec item: ${subId} / ${itemKey}`);
  }
  return baseRow + WEEK_OFFSETS_ELEC[subId][weekIndex];
}

